    private void btnsuathongtinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuathongtinActionPerformed
        if (Suathongtin == true) {

            btnsuathongtin.setText("Lưu");
            txtthongtinhovaten.setEnabled(true);
            txtthongtinEmail.setEnabled(true);
            txtthongtinngay.setEnabled(true);
            txtthongtinthang.setEnabled(true);
            txtthongtinnam.setEnabled(true);
            lbldiachi.setEnabled(true);
            txtthongtingioitinh.setEnabled(true);
            Suathongtin = false;

        } else {
            int option = JOptionPane.showConfirmDialog(this, "Bạn Có Muốn Cập Nhật Lại Thông Tin không");
            if (option == 0) {

                btnsuathongtin.setText("Sửa Thông Tin");

                txtthongtinhovaten.setEnabled(false);
                txtthongtinEmail.setEnabled(false);
                txtthongtinngay.setEnabled(false);
                txtthongtinthang.setEnabled(false);
                txtthongtinnam.setEnabled(false);
                lbldiachi.setEnabled(false);
                txtthongtingioitinh.setEnabled(false);
                User Sua = Const.session.get(User.class, Const.user.getId());
                Sua.setHovaTen(txtthongtinhovaten.getText());
                Sua.setEmail(txtthongtinEmail.getText());
                String NgaySinh = txtthongtinnam.getSelectedItem() + "-" + txtthongtinthang.getSelectedItem() + "-" + txtthongtinngay.getSelectedItem();  
                Sua.setNgaySinh(java.sql.Date.valueOf(NgaySinh));
                Sua.setGioitinh(txtthongtingioitinh.getSelectedItem().toString());
                Sua.setDiaChi(lbldiachi.getText());
                Const.session.getTransaction().begin();
                Const.session.save(Sua);
                Const.session.getTransaction().commit();
                Suathongtin = true;
                JOptionPane.showMessageDialog(this, "Cập Nhật Thành Công");
            } else {
                txtthongtinhovaten.setEnabled(false);
                txtthongtinEmail.setEnabled(false);
                txtthongtinngay.setEnabled(false);
                txtthongtinthang.setEnabled(false);
                txtthongtinnam.setEnabled(false);
                lbldiachi.setEnabled(false);
                txtthongtingioitinh.setEnabled(false);
                btnsuathongtin.setText("Sửa Thông Tin");
                Suathongtin = true;
                JOptionPane.showMessageDialog(this, "Cập Nhật Thất Bại");
            }

        }
    }//GEN-LAST:event_btnsuathongtinActionPerformed

