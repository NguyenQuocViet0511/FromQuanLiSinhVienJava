    private void loadDataSinhVienXemDiem() {
        Query query = Const.session.createQuery("SELECT d.MaMon,d.DiemDau,d.Diemgiua,d.DiemCuoi,d.Tongcuoi FROM SinhVien s join Diem d on s.MaSV = d.MaSV where s.MaSV=" + "'" + Const.user.getUserName() + "'");
        List<Object[]> ListResult = query.getResultList();
        DefaultTableModel model = (DefaultTableModel) tableXemDiem.getModel();
        double i;
        model.setNumRows(0);
        for (Object[] item : ListResult) {
            Vector<Object> Vector = new Vector<>();
            Vector.add(item[0]);
            Vector.add(item[1]);
            Vector.add(item[2]);
            Vector.add(item[3]);
            i = Double.parseDouble(item[4].toString());
            Vector.add(i);
            if (i < 5) {
                Vector.add("Rớt");
            } else {
                Vector.add("Qua Môn");
            }
            model.addRow(Vector);;

        }

    }

