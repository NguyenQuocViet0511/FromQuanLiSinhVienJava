    private void LoadDataDangKyThiLai() {

        Query query = Const.session.createQuery("SELECT T.ID,T.MaMon FROM  ThiLai T where T.MaSV=" + "'" + Const.user.getUserName() + "'");
        List<Object[]> ListResult = query.getResultList();
        DefaultTableModel model = (DefaultTableModel) tableXemLichDangKY.getModel();
        model.setNumRows(0);
        for (Object[] item : ListResult) {

            model.addRow(item);

        }

    }
