    private void btngiaoviennhap3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngiaoviennhap3ActionPerformed
        int option = JOptionPane.showConfirmDialog(this, "Bạn Có Muốn Nhập Điểm Không");
        if (option == 0) {
            if (txtDiemMaSV.getText().isEmpty() && txtDiẹmhovaTen.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không Được Để Trống Mã Sinh Viên và Họ Tên");
            } else {
                if (Double.valueOf(txtDiemdauky.getText()) > 10 && Double.valueOf(txtDiemgiuaky.getText()) > 10 && Double.valueOf(txtDiemcuoiky.getText()) > 10) {
                    JOptionPane.showMessageDialog(this, "Bạn nhập Vượt quá 10");
                } else {
                    if (checkDiemNhap()) {
                        JOptionPane.showMessageDialog(this, "Sinh Viên Nầy Đã Có");
                    } else {
                        if (CheckMaSVDiem()) {
                            Diem diem = new Diem();
                            diem.setMaSV(Integer.parseInt(txtDiemMaSV.getText()));
                            diem.setHovaTen(txtDiẹmhovaTen.getText());
                            diem.setMaMon(jComboMaMon.getSelectedItem().toString());

                            diem.setDiemDau(Double.parseDouble(txtDiemdauky.getText()));
                            diem.setDiemgiua(Double.parseDouble(txtDiemgiuaky.getText()));
                            diem.setDiemCuoi(Double.parseDouble(txtDiemcuoiky.getText()));
                            diem.setTongcuoi((diem.getDiemDau() + diem.getDiemgiua() + diem.getDiemCuoi()) / 3);
                            Const.session.getTransaction().begin();
                            Const.session.save(diem);
                            Const.session.getTransaction().commit();
                            JOptionPane.showMessageDialog(this, "Bạn Nhập Thành Công");
                            LoadDataDiem();
                            txtDiemMaSV.setText("");
                            txtDiẹmhovaTen.setText("");
                            txtDiemdauky.setText("");
                            txtDiemgiuaky.setText("");
                            txtDiemcuoiky.setText("");
                            jComboMaMon.setSelectedItem(-1);
                        } else {
                            JOptionPane.showMessageDialog(this, "Không Có sinh Viên Này Trong Danh Sách");
                        }

                    }

                }
            }

        } else {

        }
    }//GEN-LAST:event_btngiaoviennhap3ActionPerformed

