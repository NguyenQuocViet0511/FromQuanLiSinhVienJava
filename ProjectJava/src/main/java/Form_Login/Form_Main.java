    private Boolean CheckmaSVDiem() {
        int index = tableSinhVien.getSelectedRow();
        Const.ListDiem = null;
        Query q = Const.session.createQuery("FROM Diem");
        Const.ListDiem = q.getResultList();
        for (int i = 0; i < Const.ListDiem.size(); i++) {
            if (Const.ListDiem.get(i).getMaSV() == Integer.parseInt(tableSinhVien.getValueAt(index, 0).toString())) {
                return true;
            }
        }
        return false;

    }

